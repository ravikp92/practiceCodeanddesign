package src.main.java.com.example.demo.behaviorialdp.MomentoDP;

public class ConfigurationMomento {
    int height;
    int width;

    public ConfigurationMomento(int height, int width) {
        this.height = height;
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    
}
